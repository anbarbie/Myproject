#include "ft_list.h"

t_list	*ft_list_last(t_list *begin_list)
{
	t_list	*list;

	list = begin_list;
	if (list)
	{
		while (begin_list->next != 0)
			begin_list = begin_list->next;
		list = begin_list;
	}
	return (list);
}
