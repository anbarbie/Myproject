int		ft_count_if(char **tab, int (*f)(char *))
{
	int i;
	int cpt;

	cpt = 0;
	i = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]) == 1)
			cpt++;
		i++;
	}
	return (cpt);
}
